// DECLARING VARIABLES

var catName = "Mew";
let dogName = "Fido";
const CAMEL_NAME = "Roger"; // Constants are often capitalized

// LOGGING TO THE CONSOLE

console.log(`My cat's name is ${catName}.`);
console.log(`My dog's name is ${dogName}.`);
console.log(`My camel's name is ${CAMEL_NAME}.`);

// CHANGING VARIABLE CONTENT

let girlName = "Alexandra";
console.log(`My best friend is ${girlName}.`);
girlName = "Susie";
console.log(`My best friend is ${girlName}.`);